function [channels] = config_ring_channels(labels)
% Returns channels that are on the edge of the net

channels = {'E247', 'E251', 'E256', 'E91', 'E102',...
    'E111', 'E120', 'E133', 'E145', 'E165', 'E174', 'E187', 'E199',...
    'E208', 'E216','E229','E233','E237', 'E243','E246','E250','E255','E82',...
    'E92','E103','E112','E121','E134','E146','E156','E166','E175','E188','E200',...
    'E209','E217','E228','E232','E236','E240'};

channels = any(cell2mat(cellfun(@(x) strcmp(labels, x), channels, ...
    'uniform', false)), 2);
end

